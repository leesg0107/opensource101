#!/bin/sh
# $1: 체중(kg)
# $2: 신장(cm)

# BMI = 체중(kg) / (신장(m))²
# 계산의 정확성을 위해 10000을 곱함
height_m=`expr $2 \* $2`  # 신장의 제곱 (cm 단위)
bmi=`expr $1 \* 100000 / $height_m`  # 10000을 곱해서 소수점 계산

# 예시 계산:
# 65kg, 170cm의 경우
# bmi = 65 * 10000 / (170 * 170) = 650000 / 28900 ≈ 22.5

# BMI 판정
# 저체중: < 18.5 (185)
# 정상체중: 18.5 ~ 22.9 (185-229)
# 과체중: >= 23 (230)
if [ $bmi -lt 185 ]; then
    echo "저체중입니다."
elif [ $bmi -ge 185 -a $bmi -lt 230 ]; then
    echo "정상체중입니다."
else
    echo "과체중입니다."
fi
exit 0
